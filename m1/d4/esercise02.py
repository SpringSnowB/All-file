"""
累加
"""
count = 0
for item in range(4):
    count += item
print(count)

count = 0
for item in range(2,11,2):
    count += item
print(count)

count = 0
for item in range(1,11,3):
    count += item
print(count)

count = 0
for item in range(8,4,-1):
    count += item
print(count)

count = 0
for item in range(-1,-6,-1):
    count += item
print(count)
